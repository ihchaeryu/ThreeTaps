package com.example.threetaps;

public class MusicModal {

    // variables
    private String fileName;

    // constructor
    public MusicModal(String fileName) {
        this.fileName = fileName;
    }

    // getter
    public String getFileName() {
        return fileName;
    }

    // setter
    public void setFileName(String fileName) {
        this.fileName = fileName;
    }
}
